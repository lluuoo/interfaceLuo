package com.jite.struts.bean;

import java.util.List;

public class JobInfo extends PageInfo{
	private JobBaseInfo base;
	private JobRequireInfo req;
	private HrCompanyInfo com;
	private List<UserInfo> cands;
	public JobBaseInfo getBase() {
		return base;
	}
	public void setBase(JobBaseInfo base) {
		this.base = base;
	}
	public JobRequireInfo getReq() {
		return req;
	}
	public void setReq(JobRequireInfo req) {
		this.req = req;
	}
	public HrCompanyInfo getCom() {
		return com;
	}
	public void setCom(HrCompanyInfo com) {
		this.com = com;
	}
	public List<UserInfo> getCands() {
		return cands;
	}
	public void setCands(List<UserInfo> cands) {
		this.cands = cands;
	}
	
}
