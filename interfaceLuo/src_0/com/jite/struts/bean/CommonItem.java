package com.jite.struts.bean;

import java.util.List;

public class CommonItem {
	private String id;
	private String name;
	
	private List<CommonItem> subItems;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<CommonItem> getSubItems() {
		return subItems;
	}
	public void setSubItems(List<CommonItem> subItems) {
		this.subItems = subItems;
	}
}
